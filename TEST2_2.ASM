DATA	SEGMENT 
DATA1	DB 5,-4,0,3,100,-51 
N     	EQU $-DATA1
STRING1 DB'Y=-1 ','$' 
STRING2 DB'Y=0 ','$' 
STRING3 DB'Y=+1 ','$' 
DATA 	ENDS 

STACK 	SEGMENT
		DB 200 DUP(?) 
STACK 	ENDS 

CODE 	SEGMENT 
		ASSUME CS:CODE,DS:DATA,SS:STACK 
START: 	MOV AX,DATA 
		MOV DS,AX 
		LEA SI,DATA1 
	   
		MOV CX,N 
NEXT:  	MOV AL,[SI] 
		CMP AL,0
		JS 	NEXT1
		JZ	NEXT2
		JMP	NEXT3
NEXT1: 	LEA DX,STRING1;print-1
		MOV AH,09H 
		INT 21H
		JMP NEXT4
NEXT2: 	LEA DX,STRING2;print0
		MOV AH,09H 
		INT 21H 
		JMP NEXT4 
NEXT3: 	LEA DX,STRING3;print+1
		MOV AH,09H 
		INT 21H 
		JMP NEXT4 
NEXT4:
		INC SI 
		LOOP NEXT 
		MOV AH,4CH 
		INT 21H 
CODE 	ENDS 
		END START
